#include<bits/stdc++.h>
using namespace std;
main()
{
    cin.tie(0);
    cin.sync_with_stdio(0);
    int T,n;
    string s;
    cin>>T;
    while(T--) {
        vector<string> v;
        cin>>n;
        for(int i=0;i<n;i++) {
            cin>>s;
            v.push_back(s);
        }
    }
    return 0;
}
