template<typename K, typename V>
struct Map : public map<K, V> {
    public:
    V dv=0;

    // 構造函數，設定自定的預設值
    Map() {}
    Map(V _dv) : dv(_dv) {}

    // 重載 operator[]，實現自定義預設值
    V& operator[](const K& key) {
        if (this->find(key) == this->end()) {
            // 如果 key 不存在，插入自定義預設值
            this->insert({key, dv});
        }
        return map<K, V>::operator[](key);
    }
};
